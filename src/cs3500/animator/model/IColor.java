package cs3500.animator.model;

/**
 * To represent an interface for colors.
 */
public interface IColor {

  /**
   * returns amount of red hue in this color.
   * @return amount of red
   */
  public int getRed();

  /**
   * returns amount of blue hue in this color.
   * @return amount of blue
   */
  public int getBlue();

  /**
   * returns amount of green hue in this color.
   * @return amount of green
   */
  public int getGreen();
}
